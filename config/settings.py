def _database_config_from_env(base_dir):
    database_url = os.getenv("DATABASE_URL", "").strip()
    if not database_url:
        return _sqlite_database_config(base_dir)

    parsed = urlparse(database_url)
    scheme = parsed.scheme.lower().split("+", 1)[0]

    if scheme in {"postgres", "postgresql", "pgsql"}:
        config = {
            "ENGINE": "django.db.backends.postgresql",
            "NAME": unquote(parsed.path.lstrip("/")),
            "USER": unquote(parsed.username or ""),
            "PASSWORD": unquote(parsed.password or ""),
            "HOST": parsed.hostname or "",
            "PORT": str(parsed.port or ""),
            "CONN_MAX_AGE": _parse_int_env("DATABASE_CONN_MAX_AGE", 60),
        }
        query_params = parse_qs(parsed.query)
        if query_params.get("sslmode"):
            config["OPTIONS"] = {"sslmode": query_params["sslmode"][0]}
        return config

    if scheme in {"sqlite", "sqlite3"}:
        path = unquote(parsed.path or "")
        if path.startswith("/") and len(path) >= 3 and path[2] == ":":
            path = path[1:]
        return {
            "ENGINE": "django.db.backends.sqlite3",
            "NAME": path.lstrip("/") or (base_dir / "db.sqlite3"),
        }

    raise ValueError(f"Unsupported DATABASE_URL scheme: {parsed.scheme}")



